<script>
  import commonHeader from '../../components/CommonHeader'
  import navigator from '../../components/Navigator'
  import api from '../../services/api'
  import router from '../../router'
  import moment from 'moment'
  import axios from 'axios'
  export default {
    data: function () {
      return {
        title: '',
        teacher: '',
        startDate: '',
        endDate: '',
        dialogTableVisible: false,
        tableData: [],
        take: 10,
        currentPage: 1,
        total: 0,
        showloading: false,
        excelUrl: ''
      }
    },
    components: {
      commonHeader,
      navigator
    },
    created () {
      var date1 = ''
      if (this.startDate) {
        date1 = moment(this.startDate).format('YYYY-MM-DD')
      }
      var date2 = ''
      if (this.endDate) {
        date2 = moment(this.endDate).format('YYYY-MM-DD')
      }
      api.fetch(api.uri.getEnrollmentList, {title: this.title, teacher: this.teacher, start: date1, end: date2, skip: (this.currentPage - 1) * this.take, take: this.take}).then(data => {
        if (data.status === 1) {
          this.tableData = data.result
          this.total = data.total
        }
      })
    },
    filters: {
      formatDate (time) {
        var date = new Date(time)
        return moment(date).format('YYYY-MM-DD HH:mm:ss')
      }
    },
    methods: {
      queryEnrollment: function () {
        var date1 = ''
        if (this.startDate) {
          date1 = moment(this.startDate).format('YYYY-MM-DD')
        }
        var date2 = ''
        if (this.endDate) {
          date2 = moment(this.endDate).format('YYYY-MM-DD')
        }
        api.fetch(api.uri.getEnrollmentList, {title: this.title, teacher: this.teacher, start: date1, end: date2, skip: (this.currentPage - 1) * this.take, take: this.take}).then(data => {
          if (data.status === 1) {
            this.tableData = data.result
            this.total = data.total
          }
        })
      },
      handleCurrentChange (pageNum) {
        this.currentPage = pageNum
        this.queryAppointment()
      },
      exportEnrollment: function () {
        this.showloading = true
        var date1 = ''
        if (this.startDate) {
          date1 = moment(this.startDate).format('YYYY-MM-DD')
        }
        var date2 = ''
        if (this.endDate) {
          date2 = moment(this.endDate).format('YYYY-MM-DD')
        }
        api.fetch(api.uri.exportEnrollmentList, {title: this.title, teacher: this.teacher, start: date1, end: date2}).then(data => {
          if (data.status === 1) {
            this.excelUrl = axios.defaults.imageServer + data.result
            this.showloading = false
            this.dialogTableVisible = true
          }
        })
      },
      routeByName: function (name) {
        router.push({name: name})
      },
      click: function () {
        this.dialogTableVisible = !this.dialogTableVisible
      },
      open4 () {
        const h = this.$createElement
        this.$msgbox({
          title: '消息',
          message: h('p', null, [
            h('span', null, '内容可以是 '),
            h('i', {style: 'color: teal'}, 'VNode')
          ]),
          showCancelButton: true,
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          beforeClose: (action, instance, done) => {
            if (action === 'confirm') {
              instance.confirmButtonLoading = true
              instance.confirmButtonText = '执行中...'
              setTimeout(() => {
                done()
                setTimeout(() => {
                  instance.confirmButtonLoading = false
                }, 300)
              }, 3000)
            } else {
              done()
            }
          }
        }).then(action => {
          this.$message({
            type: 'info',
            message: 'action: ' + action
          })
        })
      }
    }
  }
</script>

